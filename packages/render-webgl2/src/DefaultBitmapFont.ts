export function createDefaultBitmapFontAtlas(): DefaultBitmapFontAtlas {
  const characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789 :-.!?/';
  const columns = 11;
  const glyphWidth = 6;
  const glyphHeight = 8;
  const rows = Math.ceil(characters.length / columns);
  const width = columns * glyphWidth;
  const pixels = new Uint8Array(width * rows * glyphHeight * 4);
  [...characters].forEach((character, glyph) => {
    const pattern = PATTERNS[character] ?? PATTERNS['?'];
    if (!pattern) return;
    const originX = glyph % columns * glyphWidth;
    const originY = Math.floor(glyph / columns) * glyphHeight;
    for (let y = 0; y < 7; y += 1) for (let x = 0; x < 5; x += 1) {
      if (pattern[y * 5 + x] !== '1') continue;
      const offset = ((originY + y) * width + originX + x) * 4;
      pixels[offset] = 255; pixels[offset + 1] = 255; pixels[offset + 2] = 255; pixels[offset + 3] = 255;
    }
  });
  return Object.freeze({ characters, columns, glyphWidth, glyphHeight, pixels });
}
